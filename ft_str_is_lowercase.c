int	ft_str_is_lowercase(char *str)
{
	int s;

	s = 0;
	while (str[s] != '\0')
	{
		if (!(str[s] >= 'a' && str[s] <= 'z'))
		{
			return (0);
		}
		s++;
	}
	return (1);
}
