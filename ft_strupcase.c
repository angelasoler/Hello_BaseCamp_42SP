char	*ft_strupcase(char *str)
{
	int s;

	s = 0;
	while (str[s] != '\0')
	{
		if (str[s] >= 'a' && str[s] <= 'z')
		{
			str[s] -= 32;
		}
		s++;
	}
	return (str);
}
