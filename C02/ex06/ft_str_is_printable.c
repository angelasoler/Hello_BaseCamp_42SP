int	ft_str_is_printable(char *str)
{
	int s;

	s = 0;
	while (str[s] != '\0')
	{
		if (!(str[s] >= 32 && str[s] <= 126))
		{
			return (0);
		}
		s++;
	}
	return (1);
}
