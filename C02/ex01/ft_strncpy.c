char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int b;

	b = 0;
	while (src[b] != '\0' && b < n)
	{
		dest[b] = src[b];
		b++;
	}
	while (b < n)
	{
		dest[b] = '\0';
		b++;
	}
	return (dest);
}
